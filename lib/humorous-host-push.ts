export async function sendHumorousHostPush(clerkUserId: string, trackingToken: string, tenantSlug: string) {
  if (!isFCMConfigured() && !isPushConfigured()) return false

  try {
    const subs = await getActiveSubscriptionsForUser({ clerkUserId, roleContext: 'customer' })
    if (!subs || subs.length === 0) return false

    const devices = subs.flatMap(sub => Array.isArray(sub.devices) ? sub.devices : [])
    if (devices.length === 0) return false

    const randomMessage = HUMOROUS_MESSAGES[Math.floor(Math.random() * HUMOROUS_MESSAGES.length)]
    
    const payload = {
      title: "أحلى لمة! 🍽️",
      body: randomMessage,
      url: `/t/${tenantSlug}/track/${trackingToken}`,
      data: {
        type: 'host_humorous_push',
        url: `/t/${tenantSlug}/track/${trackingToken}`
      }
    }

    let sentAny = false
    for (const device of devices) {
      if (device.fcmToken && isFCMConfigured()) {
        const { ok, permanent } = await sendFCMToTokenDetailed(device.fcmToken, payload)
        if (ok) sentAny = true
        if (permanent) await removeDevice({ clerkUserId, roleContext: 'customer', fcmToken: device.fcmToken }).catch(() => {})
      } else if (device.webPush && isPushConfigured()) {
        const { ok, permanent } = await sendPushNotificationDetailed(device.webPush, payload)
        if (ok) sentAny = true
        if (permanent) await removeDevice({ clerkUserId, roleContext: 'customer', endpoint: device.webPush.endpoint }).catch(() => {})
      }
    }
    
    return sentAny
  } catch (error) {
    console.error('[HumorousHostPush] Error sending push to host:', error)
    return false
  }
}
